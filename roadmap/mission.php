<br/><b>Mission Statement</b>

<p>California, the main gateway to the Pacific Rim economies, will increasingly need a labor force with world language and culture competence not only to establish but also advance the state's competitive edge. The California Language Roadmap, to be a 501c3 foundation, seeks to ensure that our citizens possess expertise in at least one world language and culture other than English in order to increase their value and marketability in business, technology, science, humanities and government environments in the 21<sup>st</sup> Century. To achieve this long-term goal, the California Language Roadmap will form alliances with business, educational, and community leaders to stimulate language and culture education by informing public policy and investing in the following activities:</p>

<ul>
<li>Providing fellowships to future language teachers during their credential process in order to grow the state's language capacity.</li>

<li>Partnering with respective educational agencies to expand alternate certification processes by leveraging content delivery through new distance and other multi-media learning technologies.</li>

<li>Seeding college student interest in languages by offering paid internships, study abroad, mentoring programs, and in-service learning experiences.</li>

<li>Establishing summer language camps for elementary and middle school students in order to springboard them into higher levels of language study in high school.</li>

<li>Developing language summer institutes for business executives who have pressing needs to communicate directly with their customers both abroad and within domestic markets.</li>
</ul>

<p>California's present reliance on language outsourcing compromises the state's economic, political and social future. Either we move ahead in this new world economy or we will continue to fall behind, ceding the advantage to the fast-growing nations that are already educating their workforce to compete in a multilingual world.</p>
