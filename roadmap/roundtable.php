<p>Susan J. Duggan, President, Duggan International, and Paul V. Oliva, Founder and Chief
Consultant, Oliva Global Communications, served as co-directors of the California Language
Roadmap Business Roundtable on April 20, 2010 in San Francisco. Duggan and Oliva invited
twenty Bay Area business leaders to specify the professional-level language and cultural skill
businesses need to succeed in California`s multilingual economy.<br /><br />

Participants discussed the 21st century reality that many California businesses engage a
multi-lingual workforce and service a multi-lingual clientele. Globally focused businesses face
complex challenges that require employees with language and cultural proficiency capable of
successfully conducting business and providing services in target languages.<br /><br />

The Roundtable served as a precursor to the California Language Roadmap Summit (url) by
exploring the potential role business can play in shaping a competitive, linguistically competent
workforce in California.<br /><br />

Conclusion: BUSINESS HAS A STAKE IN A PARTNERSHIP TO SHAPE California`s
COMPETITIVE WORKFORCE<br /><br />

Rountable participants concurred that the time is ripe for a public-private partnership to
move the California Language Roadmap forward. As the federal government invests in
resources to inspire and support innovative models of language acquisition, business should
look at how foreign language impacts its balance sheet and help shape policies, programs, and
actions for a competitive California workforce.<br /><br />

